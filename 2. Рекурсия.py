#Задание 1

def print_numbers(n, current=1): # Функция принимает число n и текущее число (1)
    print(current) # Печатаем текущее число
    current += 1 # Увеличиваем текущее число на 1
    if current <= n: # Проверяем не достигли ли конечное число n
        print_numbers(n,current) # Рекурсивно вызываем эту функцию с обновленным текущим числом


print("Задание1:")
print_numbers(23) # Вызываем функцию для печати чисел от 1 до 23


