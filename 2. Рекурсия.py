def summ(n,current=1): # Функция принимает число n и текущее число (1)
    current += 1 # Увеличиваем текущее число на 1
    if current <= n: # Проверяем не привысили ли число n
        if current == n: # Если текущее число равно n, то достигли конца
            return current # Возвращаем текущее число
        else: # Если текущее число меньше n, то еще не конец
            return current + summ(n, current) # Возвращаем текущее число + результат рекурсивного вызова


print("Задание3")
print(summ(4)) # Вызываем функцию для вычисления суммы чисел от 1 до 4