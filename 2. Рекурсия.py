def print_nm(a,b): # Функция принимает два числа a и b
    if a < b: # Если a меньше b, значит идем по возрастанию
        print(a) # Печатаем текущее число a
        a += 1 # Увеличиваем a на 1, двигаемся к b
        if a <= b: # Проверяем не достигли ли b
            print_nm(a,b) # Рекурсивно вызываем функцию с обновленным a


print("Задание2")
print_nm(1,14) # Вызываем функцию для печати чисел от 1 до 14


